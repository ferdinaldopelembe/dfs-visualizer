	private final int ROWS = matrix.length;
	private final int COLS = matrix[0].length;
	private final int R    = 500;
	private final int PX   = R / COLS;
	private final int PY   = R / ROWS;
	
	private int[][] path 		= new int[ROWS][COLS];
	private boolean[][] visited = new boolean[ROWS][COLS];

	private JPanel mainPanel = new JPanel() {
		protected void paintComponent(Graphics g) {
			final int thickness = 1;
			Graphics2D g2 = (Graphics2D)g.create();
			
			for (int y = 0; y < ROWS; y++) {
				for (int x = 0; x < COLS; x++) {
					
					g2.setColor(matrix[y][x] == 1 ? Color.black : getBackground());
					g2.fillRect(x * PX + thickness, y * PY + thickness, PX - 2 * thickness, PY - 2 * thickness);
					
					if (path[y][x] != 1)
						continue;
					
					g2.setColor(Color.blue);
					g2.fillRect(x * PX, y * PY, PX, PY);
				}
			}
			
		};
	};
	
	public boolean dfs (int x, int y) {
		
		if (x < 0 || y < 0 || x >= COLS || y >= ROWS)
			return false;
		
		if (visited [y][x] || matrix[y][x] == 1)
			return false;
		
		visited[y][x] = true;
		path[y][x] = 1;
		
		paintCurrentPath();
		
		if (dfs(x+1,y) || dfs(x, y+1) || dfs(x - 1,y) || dfs(x,y - 1))
			return true;
		
		if (y == ROWS - 1 && x == COLS - 1)
			return true;
		
		path[y][x] = 0;
		return false;

	}
	
	private void paintCurrentPath() {
		repaint();
		revalidate();
		try {
			Thread.sleep(40);
		} catch (InterruptedException e) {
			e.printStackTrace();
		}
	}

	public DFSGUI () {
		initFrame();
		dfs(0, 0);
	}
	
	private void initFrame () {
		mainPanel.setBackground(Color.white);
		mainPanel.setPreferredSize(new Dimension(PX * COLS, PY * ROWS));
		setContentPane(mainPanel);
		pack();
		setLocationRelativeTo(null);
		setTitle("Visualize DFS");
		setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
		setVisible(true);
	}
	
	public static void main(String[] args) {
		new DFSGUI();
	}
